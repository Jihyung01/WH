export function getRandomDialogue(characterType: string, situation: DialogueSituation): string {
  const charKey = CHARACTER_TYPE_MAP[characterType] ?? 'dodam';
  const lines = DIALOGUES[charKey]?.[situation] ?? DIALOGUES.dodam.app_open;
  return lines[Math.floor(Math.random() * lines.length)];
}
